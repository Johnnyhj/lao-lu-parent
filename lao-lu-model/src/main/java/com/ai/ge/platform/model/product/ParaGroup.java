package com.ai.ge.platform.model.product;

public class ParaGroup {
    private Long paraSpecGroupCd;

    private String name;

    private Short statusCd;

    public Long getParaSpecGroupCd() {
        return paraSpecGroupCd;
    }

    public void setParaSpecGroupCd(Long paraSpecGroupCd) {
        this.paraSpecGroupCd = paraSpecGroupCd;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Short getStatusCd() {
        return statusCd;
    }

    public void setStatusCd(Short statusCd) {
        this.statusCd = statusCd;
    }
}