package com.ai.ge.platform.model.sys;

public class SystemInterfaceConfig {
    private Long interfaceConfigId;

    private String interfaceChannel;

    private String interfaceConfigCode;

    private String interfaceConfigName;

    private String interfaceConfigUrl;

    private String requestTemplateUrl;

    private Short interfaceConfigVersion;

    private String interfaceInstance;

    private String interfaceUsername;

    private String interfacePassword;

    public Long getInterfaceConfigId() {
        return interfaceConfigId;
    }

    public void setInterfaceConfigId(Long interfaceConfigId) {
        this.interfaceConfigId = interfaceConfigId;
    }

    public String getInterfaceChannel() {
        return interfaceChannel;
    }

    public void setInterfaceChannel(String interfaceChannel) {
        this.interfaceChannel = interfaceChannel;
    }

    public String getInterfaceConfigCode() {
        return interfaceConfigCode;
    }

    public void setInterfaceConfigCode(String interfaceConfigCode) {
        this.interfaceConfigCode = interfaceConfigCode;
    }

    public String getInterfaceConfigName() {
        return interfaceConfigName;
    }

    public void setInterfaceConfigName(String interfaceConfigName) {
        this.interfaceConfigName = interfaceConfigName;
    }

    public String getInterfaceConfigUrl() {
        return interfaceConfigUrl;
    }

    public void setInterfaceConfigUrl(String interfaceConfigUrl) {
        this.interfaceConfigUrl = interfaceConfigUrl;
    }

    public String getRequestTemplateUrl() {
        return requestTemplateUrl;
    }

    public void setRequestTemplateUrl(String requestTemplateUrl) {
        this.requestTemplateUrl = requestTemplateUrl;
    }

    public Short getInterfaceConfigVersion() {
        return interfaceConfigVersion;
    }

    public void setInterfaceConfigVersion(Short interfaceConfigVersion) {
        this.interfaceConfigVersion = interfaceConfigVersion;
    }

    public String getInterfaceInstance() {
        return interfaceInstance;
    }

    public void setInterfaceInstance(String interfaceInstance) {
        this.interfaceInstance = interfaceInstance;
    }

    public String getInterfaceUsername() {
        return interfaceUsername;
    }

    public void setInterfaceUsername(String interfaceUsername) {
        this.interfaceUsername = interfaceUsername;
    }

    public String getInterfacePassword() {
        return interfacePassword;
    }

    public void setInterfacePassword(String interfacePassword) {
        this.interfacePassword = interfacePassword;
    }
}